<?php

namespace App\Queries\Products;

use App\Queries\IQuery;

class GetProductQuery implements IQuery
{
    protected int $id;

    /**
     * Get the value of id
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set the value of id
     *
     * @return  self
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }
}